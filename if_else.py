import  sys

# Программа росчета покупок мамы с использованием оператора if и else 
# Так же в данной задаче простенько реализована проверка

print('В этой прорамме нужно вводить данные текстом на русском язеке или цифрами')
print('Программа для расчета затрат мамы')
# Определим была совершина покупка или нет                                      
purchases = str(input('Мама купила что нибуть (да или нет): '))

# Провеим правильно был сделан ввод                                             
if purchases == 'да':
    print('Ошибок нет, программа продолжает выполнятся')
else:
    print('Мама ничего не купила или не правильный ввод, программа завершает работу')
    sys.exit()


# Знакомая часть программы                                                      
product = str(input('что мама купила: '))
