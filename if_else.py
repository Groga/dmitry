summ = volume*price
print('Мама купила ', product, volume,  ' кг или шт ',  'по цене ', price, ' за кг или шт, на сумму: ', summ)

# Новое продолжение программы                                                   
# В этой части программы выясним остались у мамы деньги после покупок или нет   
salary = int(input('Сколько мама сегодня заработала?: '))
money = salary-summ
if money>0:
    print('Мама молодец')
